package xmlDom;

public class InspectPackItemBean {
    private  String inspectStatus;
    private  String inspectRes;

    private String inspectCompute;
    private String inspectComputeValue;
    private String inspectComputeStatus;

    public String getInspectStatus() {
        return inspectStatus;
    }

    public void setInspectStatus(String inspectStatus) {
        this.inspectStatus = inspectStatus;
    }

    public String getInspectRes() {
        return inspectRes;
    }

    public void setInspectRes(String inspectRes) {
        this.inspectRes = inspectRes;
    }

    public String getInspectCompute() {
        return inspectCompute;
    }

    public void setInspectCompute(String inspectCompute) {
        this.inspectCompute = inspectCompute;
    }

    public String getInspectComputeValue() {
        return inspectComputeValue;
    }

    public void setInspectComputeValue(String inspectComputeValue) {
        this.inspectComputeValue = inspectComputeValue;
    }

    public String getInspectComputeStatus() {
        return inspectComputeStatus;
    }

    public void setInspectComputeStatus(String inspectComputeStatus) {
        this.inspectComputeStatus = inspectComputeStatus;
    }
}
